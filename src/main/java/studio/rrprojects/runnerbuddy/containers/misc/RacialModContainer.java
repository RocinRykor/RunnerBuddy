package studio.rrprojects.runnerbuddy.containers.misc;

public class RacialModContainer {
    public int modBod;
    public int modQuick;
    public int modStr;
    public int modCha;
    public int modInt;
    public int modWil;

    public RacialModContainer(int bod, int quick, int str, int cha, int Int, int wil) {
        modBod = bod;
        modQuick = quick;
        modStr = str;
        modCha = cha;
        modInt = Int;
        modWil = wil;
    }
}
